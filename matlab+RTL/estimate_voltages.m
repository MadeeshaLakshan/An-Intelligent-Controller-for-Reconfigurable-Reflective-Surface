function estimated_voltages = estimate_voltages(irs_phase_shifts)
    raw_voltages = [0, 1, 2, 3, 4, 5, 6, 7, 8, 8.1, 8.2, 8.5, 8.7, 9, 9.2, ...
                9.5, 9.7, 9.8, 10, 10.2, 10.5, 10.7, 11, 11.2, 11.5, ...
                11.7, 12, 12.2, 12.5, 12.7, 13, 13.2, 13.5, 13.7, 14, ...
                14.2, 14.5, 14.7, 15, 15.2, 15.5, 15.7, 16, 16.2, 16.5, ...
                16.7, 17, 17.2, 17.5, 17.7, 18, 18.2, 18.5, 18.7];

    phases_deg = [-164.495, -162.145, -161.325, -158.655, -156.975, -154.065, ...
                  -150.245, -142.805, -125.765, -124.565, -120.565, -109.715, ...
                  -101.205, -79.685, -57.695, -3.545, 45.845, 56.615, 84.665, ...
                  98.195, 110.655, 118.285, 128.635, 131.875, 137.355, 138.935, ...
                  144.305, 146.465, 148.915, 149.915, 151.905, 152.625, 154.125, ...
                  154.745, 156.195, 156.485, 157.605, 158.015, 158.845, 159.525, ...
                  159.735, 161.055, 161.245, 162.065, 162.395, 162.655, 163.345, ...
                  163.515, 164.025, 164.275, 164.105, 164.325, 164.375, 164.495];

    % Convert IRS phase shifts to degrees
    irs_phases_deg = rad2deg(irs_phase_shifts);

    % Interpolate without extrapolation
    estimated_voltages = interp1(phases_deg, raw_voltages, irs_phases_deg, 'linear');

    % Clamp values to min and max of raw_voltages
    minV = min(raw_voltages);
    maxV = max(raw_voltages);
    estimated_voltages = max(minV, min(maxV, estimated_voltages));
end
